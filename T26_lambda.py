# One line function

double = lambda x : x * 2

print(double(5))

# Output
# 10


# _______________________________________________________________________________

cube = lambda x : x * x * x

print(cube(3))

# Output
# 27

# _______________________________________________________________________________

avg = lambda x,y : (x + y) / 2

print(avg(3,5))

# Output
# 4.0

